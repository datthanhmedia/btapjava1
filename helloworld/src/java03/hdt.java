package java03;

public class hdt {
	private String name;
	private int age;
	private String type;
	private int numFruit;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getNumFruit() {
		return numFruit;
	}
	public void setNumFruit(int numFruit) {
		this.numFruit = numFruit;
	}
	public String getType() {
		return type;
	}
	

}
